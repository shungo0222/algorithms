from graphviz import Digraph
from IPython.display import Image

g = Digraph(format='png')

x = [6, 5, 4, 4, 3, 3, 2, 3, 2, 2, 1, 2, 1, 
     1, 0, 2, 1, 1, 0, 1, 0, 1, 0, 1, 0]

for i in range(25):
    g.node(str(i+1), str(x[i]))

y = [('1', '2'), ('1', '3'), ('2', '4'), ('2', '5'), ('3', '6'),
('3', '7'), ('4', '8'), ('4', '9'), ('5', '10'), ('5', '11'),
('6', '12'), ('6', '13'), ('7', '14'), ('7', '15'), ('8', '16'),
('8', '17'), ('9', '18'), ('9', '19'), ('10', '20'), ('10', '21'),
('12', '22'), ('12', '23'), ('16', '24'), ('16', '25')]

for a, b, in y:
    g.edge(a, b)

g.render('tree')

Image('tree.png')